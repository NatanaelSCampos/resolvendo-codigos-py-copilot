# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

# Solicitar ao usuário que insira duas strings
string1 = input("Digite a primeira string: ")
string2 = input("Digite a segunda string: ")

# Concatenar as strings
string_concatenada = string1 + " " + string2  # Adiciona um espaço entre as strings

# Exibir a string concatenada
print("A string concatenada é:", string_concatenada)
